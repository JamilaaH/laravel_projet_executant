    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            "url"=>['required'],
            "categorie"=>"required",
        ]);
        $request->file('url')->storePublicly('img/galerie', 'public');
        $galerie = new Galerie();
        $galerie->url = $request->file('url')->hashName();
        $galerie->categorie_id = $request->categorie;
        $galerie->save();
        return redirect()->route('galerie.index');
    }

